-- ==========================================

-- 1. 创建车辆信息表
CREATE TABLE vehicle_infos (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  owner_name VARCHAR(100) NOT NULL,
  license_plate VARCHAR(20) NOT NULL UNIQUE,
  phone VARCHAR(20),
  department VARCHAR(50),
  description TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 2. 创建索引（提高查询性能）
CREATE INDEX idx_vehicle_infos_license_plate ON vehicle_infos(license_plate);
CREATE INDEX idx_vehicle_infos_department ON vehicle_infos(department);
CREATE INDEX idx_vehicle_infos_created_at ON vehicle_infos(created_at DESC);

-- 3. 创建复合索引（优化常用查询）
CREATE INDEX idx_vehicle_infos_department_created ON vehicle_infos(department, created_at DESC);

-- 4. 启用RLS（行级安全）- 可选
ALTER TABLE vehicle_infos ENABLE ROW LEVEL SECURITY;

-- 5. 创建允许所有操作的策略（开发阶段）
CREATE POLICY "Allow all access" ON vehicle_infos
FOR ALL USING (true);

