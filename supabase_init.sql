-- 7. 创建查询视图（可选，方便查看数据统计）
CREATE VIEW vehicle_stats AS
SELECT
  department,
  COUNT(*) as vehicle_count,
  MIN(created_at) as first_added,
  MAX(created_at) as last_added
FROM vehicle_infos
GROUP BY department
ORDER BY vehicle_count DESC;

